SELECT E.FIRST_NAME, E.LAST_NAME, E.SALARY, J.JOB_TITLE 
FROM EMPLOYEES E JOIN JOBS J
ON E.JOB_ID = J.JOB_ID
AND J.JOB_TITLE = 'Programmer';