SELECT J.JOB_TITLE, 
       COUNT(*) AS CALISAN_SAYISI, 
       SUM(E.SALARY) AS TOPLAM_MAAS, 
       ROUND(AVG(E.SALARY), 2) AS ORTALAMA_MAAS
FROM EMPLOYEES E, JOBS J
WHERE E.JOB_ID = J.JOB_ID
GROUP BY J.JOB_TITLE
HAVING COUNT(*) > 3;

